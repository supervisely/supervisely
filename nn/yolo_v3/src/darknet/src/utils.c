const char * timest_str()
{
    static char buffer[64];
    static char buffer_out[64];
    struct timeval tmval;
    gettimeofday(&tmval, NULL);
    int msec = (int)(tmval.tv_usec / 1000.0);

    struct tm * tm_info = localtime(&tmval.tv_sec);
    strftime(buffer, 63, "%Y-%m-%dT%H:%M:%S", tm_info);
    snprintf(buffer_out, 63, "%s.%03dZ", buffer, msec);
    return buffer_out;
}

